		}
		mapping = new Dictionary<string, byte>(names.Length);
		for (int i = 0; i < names.Length; i++)
		{
			if (names[i] != null)
			{
				mapping[names[i]] = (byte)i;
			}
		}
	}

	public string[] GetNames()
	{
		return (string[])names.Clone();
	}

	public byte GetCharId(string name)
	{
		return mapping[name];
	}
