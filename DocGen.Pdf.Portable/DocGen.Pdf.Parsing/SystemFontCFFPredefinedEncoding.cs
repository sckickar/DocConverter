		StandardEncoding.sids[254] = 0;
		StandardEncoding.sids[255] = 0;
	}

	static SystemFontCFFPredefinedEncoding()
	{
		InitializeStandardEncoding();
		InitializeExpertEncoding();
	}

	public SystemFontCFFPredefinedEncoding()
	{
		sids = new ushort[256];
	}

	public SystemFontPostScriptArray ToArray()
	{
		SystemFontPostScriptArray systemFontPostScriptArray = new SystemFontPostScriptArray(sids.Length);
		for (int i = 0; i < sids.Length; i++)
		{
			systemFontPostScriptArray.Add(sids[i]);
		}
		return systemFontPostScriptArray;
	}

	public string GetGlyphName(SystemFontCFFFontFile fontFile, ushort index)
	{
		if (index >= sids.Length)
		{
			return ".notdef";
		}
		return fontFile.ReadString(sids[index]);
	}
